export function getCategory(ingredientName) {
  return ingredientCategoryMap[ingredientName] || '其他'
}

export function parseAmount(amountStr) {
  if (!amountStr || amountStr === '适量' || amountStr === '少许') {
    return { value: null, unit: amountStr || '适量' }
  }
  
  const match = amountStr.match(/([\d.]+)\s*(\S+)/)
  if (match) {
    return {
      value: parseFloat(match[1]),
      unit: match[2].trim()
    }
  }
  
  return { value: null, unit: amountStr }
}

export function formatAmount(value, unit) {
  if (value === null || value === undefined) {
    return unit || '适量'
  }
  return `${value}${unit || ''}`
}
